#include "libft.h"

char	*ft_toupper(char *str)
{
	int	r;

	r = 0;
	while (str[r])
	{
		if (str[r] >= 'a' && str[r] <= 'z')
			str[r] -= 32;
		r++;
	}
	return (str);
}
